    <h3>Douaniers et Sauveteurs</h3>
    Si les pilotes et les lamaneurs représentent la cheville ouvrière du
    sauvetage en mer institutionnalisé,  les douaniers qui sont les acteurs
    incontournables du sauvetage individuel participent eux aussi aux
    sauvetages des navires au large du Dunkerquois.
    <br>
    Présents dans les ports et sur tout le littoral pour empêcher d’éventuels
    déchargements illégaux, embarqués à bord de leurs pataches (équivalent des
    vedettes des douanes d’aujourd’hui), les douaniers armaient également les
    stations porte-amarre de Cayenne (localisation estimée du côté du pilotage
    de nos jours) – Risban Ouest – Leffrinckoucke – Mardyck et Grand-Fort-
    Philippe. Ces stations étaient destinées à porter secours aux bateaux en
    perdition à proximité de la côte en mettant en place un système de
    va-et-vient et bouée culotte à l’aide d’un  fusil ou d’un canon lance amarre.
    <br>
    Leur présence sur les quais des ports du dunkerquois ont permis de sauver
    nombre de vies de marins, d’ouvriers du port, d’enfants…. qui pour des
    raisons variées ont chu dans les eaux portuaires ou parfois dans les canaux.
    <br>
    Ainsi sur 1027 sauvetages individuels recensés entre 1824 – 1904, ils ont
    permis de sauver plus de 546 personnes.
    <br>
    Ils étaient équipés dans les années 1860 de lignes Torrès ou de gaffes
    Legrand.
    <br>
    Ces moyens de secours, relativement encombrants, ont été rapidement
    supplantés, dans les années 1870, par la ligne Brunel, inventé par l’ancien
    lieutenant des douanes Brunel, qui présentait l’avantage de pouvoir être
    portée, dans son étui, au ceinturon des douaniers en faction. On en retrouve
    parfois l’usage en complément de la ligne Torrès, pour finaliser un sauvetage
     opéré par ligne Brunel.
